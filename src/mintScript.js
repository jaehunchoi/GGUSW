export async function publicMint() {
    if (window.klaytn.networkVersion === 8217) {
        console.log("메인넷");
    } else if (window.klaytn.networkVersion === 1001) {
        console.log("테스트넷");
    } else {
        alert("ERROR: 클레이튼 네트워크로 연결되지 않았습니다!");
        return;
    }
    if (!account) {
        alert("ERROR: 지갑을 연결해주세요!");
        return;
    }

    const myContract = new window.caver.klay.Contract(ABI, CONTRACTADDRESS);
    const amount = document.getElementById('amount').value;
    await check_status();
    if (maxSaleAmount + 1 <= mintIndexForSale) {
        alert("모든 물량이 소진되었습니다.");
        return;
    } else if (blockNumber <= mintStartBlockNumber) {
        alert("아직 민팅이 시작되지 않았습니다.");
        return;
    } else if (mintLimitPerBlock<amount){
        alert("최대 민팅수량이 초과 했습니다.");
        return;
    }   
    const total_value = amount * mintPrice;

    let estmated_gas;

    await myContract.methods.publicMint(amount)
        .estimateGas({
            from: account,
            gas: 6000000,
            value: total_value.toString()
        })
        .then(function (gasAmount) {
            estmated_gas = gasAmount;
            console.log("gas :" + estmated_gas);
            myContract.methods.publicMint(amount)
                .send({
                    from: account,
                    gas: estmated_gas,
                    value: total_value.toString()
                })
                .on("transactionHash", (txid) => {
                    console.log(txid);
                })
                .once("allEvents", (allEvents) => {
                    console.log(allEvents);
                })
                .once("Transfer", (transferEvent) => {
                    console.log(transferEvent);
                })
                .once("receipt", (receipt) => {
                    alert("민팅에 성공하였습니다.");
                })
                .on("error", (error) => {
                    //if(erroralert("민팅에 실패하였습니다.");
                    console.log(error);
                });
        })
        .catch(function (error) {
            console.log(error);
            //alert("민팅에 실패하였습니다.");
        });
}
